<p>At the right side of the application, you can find some main functions of the application which are:</p>

<ul>
    <li><b>Save:</b> saves all the data you have entered in your account in the database.</li>
    <li><b>Clear:</b> clears all the data you have entered since you started using the application.</li>
    <li><b>Download:</b> data is downloaded in an excel file that includes the main 
        table of the overall budgeting and as many additional sheets as the 
        priorities you have defined with the corresponing table. </li>
    <li><b>Publish:</b> your data is published in the platform and, in this way, any 
        user can see your budget details. This is a very important step in case 
        you want to benchmark your region with another region. </li>
</ul>

<p>At any time, you can show only budget values or additional information, 
    regarding (i) the thematic objective, (ii) the intervention field, (iii) the 
    form of finance and (iv) the funding source, or even add a comment for each 
    priority.</p>

