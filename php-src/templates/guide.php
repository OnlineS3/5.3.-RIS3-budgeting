
<h2>How to use this application step-by-step?</h2>


        
<p class='guide-steps' style='margin-top:28px !important'>
    Step 1 - Selection of the region and years for the elaboration of the RIS3 Budgeting
</p>
<p>
    First, you have to select the region for the RIS3 budget you are going to plan.
    Then, you have to choose the years for which you want to elaborate the RIS3 
    financial plan.  Bear in mind that you should select the years in 
    chronological order, so that the budgeting tables are properly presented.
</p>

